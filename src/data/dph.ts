const HOUR_LABELS = [
  '09:00-10:00', '10:00-11:00', '11:00-12:00', '12:00-13:00',
  '13:00-14:00', '14:00-15:00', '15:00-16:00', '16:00-17:00',
  '17:00-18:00', '18:00-19:00', '19:00-20:00', '20:00-21:00',
  '21:00-22:00', '22:00-23:00', '23:00-00:00', '00:00-01:00',
]

const DAY_LABELS_MAP: Record<string, string> = {
  segunda: 'Segunda-feira',
  terca: 'Terça-feira',
  quarta: 'Quarta-feira',
  quinta: 'Quinta-feira',
  sexta: 'Sexta-feira',
  sabado: 'Sábado',
  domingo: 'Domingo',
}

function buildDayDemand(day: string, raw: number[][]): DayDemand {
  const hours = raw.map((r, i) => ({
    hour: HOUR_LABELS[i],
    orders: r[0],
    people: r[1],
    ordersPerPerson: r[2],
    costPerHour: r[3],
    costPerOrder: r[4],
    employeeCosts: [],
  }))

  const totalOrders = hours.reduce((s, h) => s + h.orders, 0)
  const totalCost = hours.reduce((s, h) => s + h.costPerHour, 0)
  const peakPeople = Math.max(...hours.map(h => h.people))

  return {
    day,
    dayLabel: DAY_LABELS_MAP[day],
    hours,
    totalOrders,
    totalCost,
    avgCostPerOrder: totalOrders > 0 ? totalCost / totalOrders : 0,
    peakPeople,
  }
}

export const week1Data: DayDemand[] = Object.entries(week1Raw).map(
  ([day, raw]) => buildDayDemand(day, raw)
)

export function getDayData(day: string): DayDemand | undefined {
  return week1Data.find(d => d.day === day)
}

export function getWeekSummary() {
  const totalOrders = week1Data.reduce((s, d) => s + d.totalOrders, 0)
  const totalCost = week1Data.reduce((s, d) => s + d.totalCost, 0)
  const avgCPP = totalOrders > 0 ? totalCost / totalOrders : 0
  const peakDay = week1Data.reduce((max, d) => d.totalOrders > max.totalOrders ? d : max, week1Data[0])

  return { totalOrders, totalCost, avgCPP, peakDay }
}
